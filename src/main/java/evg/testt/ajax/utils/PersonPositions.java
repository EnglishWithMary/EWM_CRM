package evg.testt.ajax.utils;

public class PersonPositions {
    int position;
    int id;

    public PersonPositions() {
    }

    public PersonPositions(int position, int id) {
        this.position = position;
        this.id = id;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
